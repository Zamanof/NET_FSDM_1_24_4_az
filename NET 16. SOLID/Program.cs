/*
 S.O.L.I.D.
    
    1. Single Responsibilty Principle (SRP) - Vahid məsuliyyət prinsipi.
        - There should never be more than one reason for a class to change.
        - class-ın dəyişdirilməsi üçün yalnız bir səbəb olmalıdr.
    
    2. Open-Closed Principle (OCP) - Açıq/Qapalı Prinsipi
        - Software entities ... should be open for extension, but closed for modification
        - Proqramda olan varlıqlar genişlənməyə açıq, dəyişikliyə qapalı olmalıdır

    3. Liskov Substitution Principle (LSP) - Liskovun əvəzetmə prinsipi
        - Functions that use pointers or references to base classes must be able to use objects of derived classes without knowing it
        - Baza tipin istifadə etdiyi funksionalları, alttiplərimiz xəbərləri olmas da belə istifadə edə bilməlidir

    4. Interface Segregation Principle (ISP) - Interfeyslərin ayrılması prinsipi
        - Clients should not be forced to depend upon interfaces that they do not use.
        - Client üçün nəzərdə tutulmuş çox sayda interfeys, bir ümumi interfeysdən daha yaxşıdır

    5. Dependency Inversion Principle (DIP) - asılılıqların inversiyası prinsipi
        - States to depend upon abstractions, not concretes
        - Asılılıq abstraksiyadan asılı olmalıdır. Nədənsə konkret asılılıq olmalı deyil.

        - High-level modules should not depend on low-level modules. Both should depend on the abstraction.

        - Abstractions should not depend on details. Details should depend on abstractions.
 */

